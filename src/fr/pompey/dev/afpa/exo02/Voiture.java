package fr.pompey.dev.afpa.exo02;

public class Voiture {


    public char marque;
    public double prix;

    public Voiture() {

    };

    // Getter & Setter
    public char getMarque() {
        return marque;
    }

    public void setMarque(char marque) {
        this.marque = marque;
    }

    public double getPrix() {
        return prix;
    }

    public void setPrix(double prix) {
        this.prix = prix;
    }



}
